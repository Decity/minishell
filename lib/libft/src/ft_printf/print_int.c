#include "ft_printf.h"
#include <limits.h>

int	print_int(int n)
{
	int	i;

	i = 0;
	if (n == INT_MIN)
		return (print_str("-2147483648"));
	if (n < 0)
	{
		i += print_char('-');
		n *= -1;
	}
	if (n >= 10)
		i += print_int(n / 10);
	return (i + print_char(('0' + (n % 10))));
}
